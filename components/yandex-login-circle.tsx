"use client"

import React, { useState } from "react"
import styled from "styled-components"
import { toast } from "sonner"

type YandexLoginCircleProps = {
  onClick: () => void
}

const Wrapper = styled.div`
  .loader-wrapper {
    position: relative;
    display: flex;
    align-items: center;
    justify-content: center;
    width: 270px;
    height: 270px;
    font-family: "Inter", sans-serif;
    font-size: 2.4em;
    font-weight: 300;
    color: white;
    border-radius: 50%;
    background-color: transparent;
    user-select: none;
    cursor: pointer;
    border: 0;
    padding: 0;
  }

  .loader-wrapper:focus-visible {
    outline: 2px solid rgba(255, 255, 255, 0.85);
    outline-offset: 3px;
  }

  .loader-wrapper:disabled {
    cursor: not-allowed;
    opacity: 0.7;
  }

  .loader {
    position: absolute;
    top: 0;
    left: 0;
    width: 100%;
    aspect-ratio: 1 / 1;
    border-radius: 50%;
    background-color: transparent;
    animation: loader-rotate 2s linear infinite;
    z-index: 0;
  }

  @keyframes loader-rotate {
    0% {
      transform: rotate(90deg);
      box-shadow:
        0 10px 20px 0 #fff inset,
        0 20px 30px 0 #ad5fff inset,
        0 60px 60px 0 #471eec inset;
    }
    50% {
      transform: rotate(270deg);
      box-shadow:
        0 10px 20px 0 #fff inset,
        0 20px 10px 0 #d60a47 inset,
        0 40px 60px 0 #311e80 inset;
    }
    100% {
      transform: rotate(450deg);
      box-shadow:
        0 10px 20px 0 #fff inset,
        0 20px 30px 0 #ad5fff inset,
        0 60px 60px 0 #471eec inset;
    }
  }

  .loader-letter {
    display: inline-block;
    opacity: 0.4;
    transform: translateY(0);
    animation: loader-letter-anim 2s infinite;
    z-index: 1;
    border-radius: 50ch;
    border: none;
  }

  .loader-letter:nth-child(1) { animation-delay: 0s; }
  .loader-letter:nth-child(2) { animation-delay: 0.1s; }
  .loader-letter:nth-child(3) { animation-delay: 0.2s; }
  .loader-letter:nth-child(4) { animation-delay: 0.3s; }
  .loader-letter:nth-child(5) { animation-delay: 0.4s; }

  @keyframes loader-letter-anim {
    0%, 100% { opacity: 0.4; transform: translateY(0); }
    20% { opacity: 1; transform: scale(1.15); }
    40% { opacity: 0.7; transform: translateY(0); }
  }
`

export function YandexLoginCircle({ onClick }: YandexLoginCircleProps) {
  const [isSubmitting, setIsSubmitting] = useState(false)

  const handleClick = async () => {
    if (isSubmitting) return

    try {
      setIsSubmitting(true)
      await onClick()
    } catch (error) {
      const message = error instanceof Error ? error.message : "Ошибка при входе через Яндекс"
      toast.error(message)
    } finally {
      setIsSubmitting(false)
    }
  }

  return (
    <Wrapper>
      <button 
        type="button" 
        className="loader-wrapper" 
        onClick={handleClick}
        disabled={isSubmitting}
        aria-label="Войти через Яндекс"
      >
        <span className="loader-letter">В</span>
        <span className="loader-letter">о</span>
        <span className="loader-letter">й</span>
        <span className="loader-letter">т</span>
        <span className="loader-letter">и</span>
        <div className="loader" />
      </button>
    </Wrapper>
  )
}
